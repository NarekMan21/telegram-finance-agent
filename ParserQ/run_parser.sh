#!/bin/bash

echo "Telegram Финансовый Парсер"
echo "========================="
echo

# Проверка установлен ли Python
if ! command -v python3 &> /dev/null
then
    echo "Ошибка: Python 3 не установлен"
    echo "Пожалуйста, установите Python 3.7 или новее и попробуйте снова"
    exit 1
fi

echo "Запуск Telegram Финансового Парсера..."
echo
python3 parser.py

# Проверка не вернули ли командой выше код с ошибкой
if [ $? -ne 0 ]; then
    echo
    echo "=================================="
    echo "Если Вы увидите \"PhoneNumberInvalidError\":"
    echo "1. Убедитесь вы сделали config.json файл"
    echo "2. Проверьте что номер телефона в config.json начинается с \"+\" и код страны"
    echo "3. Убедитесь что номер телефона действителен и зарегистрирован в Telegram"
    echo
    echo "Пример действительного номера телефона: +1234567890"
    echo
    echo "Смотрите README.md для подробных инструкций по настройке"
    echo "=================================="
    echo
fi